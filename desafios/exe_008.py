"""
Escreva um programa que leia um valor em metros e converta em centimetros e milimetros

"""
metro = float(input('Digite a quantidade de metros '))
centimetro = metro * 100
milimetro = metro * 1000

print(f'Voce digitou {metro} esse valor em centimetro é {centimetro} e em milimetro é {milimetro}')



